// Functions : A function in JavaScript is a reusable block of code that performs a specific task. It's like a mini-program within your larger program.

// Syntax :
function functionName(parameters) {
	console.log("functionNameFunction");
}
functionName(); //Output :  functionNameFunction.
/*
function: Keyword to define a function.
functionName: The name you give to the function.
parameters: Optional input values for the function (separated by commas).
function body: The code that the function executes.
return: Optional keyword to specify a value to be sent back from the function.

*/

